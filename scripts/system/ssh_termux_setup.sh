    for i in "${!config_options[@]}"; do
        local num=$((i + 1))
        local config="${config_options[$i]}"
        local description="${SSH_CONFIGS[$config]}"
        echo "  [$num] $config - $description"
    done
    echo "  [4] Show current status only"
    echo "  [0] Cancel"
    echo ""
    
    while true; do
        read -p "Select SSH configuration: " choice
        
        case "$choice" in
